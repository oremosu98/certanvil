#!/usr/bin/env node
// Extract a critical-CSS subset — the exact CSS bytes used on the home page
// first paint, measured via Playwright CSS Coverage API.
//
// Strategy:
//   1. Load the home page (no navigation to other pages) — first-paint only
//   2. Collect which CSS ranges fired
//   3. Expand each range to the nearest complete rule block boundaries
//      so we never emit a partial rule
//   4. Write <out-css> containing only those rules
//
// Usage: node scripts/css-extract-critical.js [base-url] [target-css] [out-css] [viewport-width] [viewport-height]
// Defaults: http://localhost:3140  styles.css  styles-critical.css  1280  800
//
// v7.86.0 (Lighthouse-90 M2): generalized from a styles.css-only script so it
// can also extract lift-screens.css's above-fold mobile-home subset at a
// 412px viewport (the standard Lighthouse mobile width) — pass:
//   node scripts/css-extract-critical.js http://localhost:3140 lift-screens.css lift-critical.css 412 915
// lift-screens.css only applies under `media="(max-width: 899px)"`, so it
// must be extracted at a mobile viewport or Playwright's CSS Coverage API
// will report zero used ranges (the stylesheet's media query won't match a
// desktop-sized page and the browser never evaluates its rules as "used").

const { chromium } = require('@playwright/test');
const fs = require('fs');
const path = require('path');

const BASE = process.argv[2] || 'http://localhost:3140';
const TARGET = process.argv[3] || 'styles.css';
const OUT_NAME = process.argv[4] || 'styles-critical.css';
const VP_WIDTH = parseInt(process.argv[5], 10) || 1280;
const VP_HEIGHT = parseInt(process.argv[6], 10) || 800;
const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, OUT_NAME);
// Match the exact target file, not any stylesheet whose URL merely contains
// it as a substring (e.g. TARGET=lift-screens.css must not also match a
// future lift-screens-critical.css derivative).
const targetBase = TARGET.replace(/\?.*$/, '');


async function main() {
  console.log(`\nExtracting home-page critical CSS from ${TARGET} @ ${VP_WIDTH}x${VP_HEIGHT}...\n`);

  const browser = await chromium.launch({ headless: true });
  const page = await browser.newPage({ viewport: { width: VP_WIDTH, height: VP_HEIGHT } });

  await page.coverage.startCSSCoverage({ resetOnNavigation: false });

  // Home page ONLY — we want first-paint critical CSS, not SPA navigation CSS
  await page.goto(BASE, { waitUntil: 'networkidle', timeout: 20000 });
  await page.waitForSelector('#page-setup.active', { timeout: 10000 }).catch(() => {});
  // Small wait for render functions to run and apply CSS
  await page.waitForTimeout(1200);

  const coverage = await page.coverage.stopCSSCoverage();
  await browser.close();

  // Find the target stylesheet's entry (exact basename match, sans query string).
  const entry = coverage.find(e => {
    const urlBase = e.url.split('/').pop().replace(/\?.*$/, '');
    return urlBase === targetBase;
  });
  if (!entry) {
    console.error(`${TARGET} not found in coverage. Is the server running at ${BASE}? (viewport ${VP_WIDTH}x${VP_HEIGHT} — check the stylesheet's media query actually matches this size)`);
    process.exit(1);
  }

  const text = entry.text;
  console.log(`${TARGET}: ${(text.length / 1024).toFixed(1)}KB total`);
  console.log(`Coverage ranges: ${entry.ranges.length} segments used`);

  // Merge overlapping ranges and collect used chars
  const usedChars = new Set();
  for (const r of entry.ranges) {
    for (let i = r.start; i < r.end; i++) usedChars.add(i);
  }
  console.log(`Used: ${(usedChars.size / 1024).toFixed(1)}KB`);

  // Playwright coverage ranges already represent complete parsed rule blocks —
  // the browser only marks whole rules as used. No expansion needed; just
  // sort + merge overlapping ranges and extract the raw slices.
  const sortedRanges = [...entry.ranges].sort((a, b) => a.start - b.start);

  // Merge adjacent/overlapping ranges (gap ≤ 2 chars = whitespace between rules)
  const merged = [];
  for (const r of sortedRanges) {
    if (merged.length && r.start <= merged[merged.length - 1].end + 2) {
      merged[merged.length - 1].end = Math.max(merged[merged.length - 1].end, r.end);
    } else {
      merged.push({ start: r.start, end: r.end });
    }
  }

  // Extract raw segments
  const segments = [];
  for (const r of merged) {
    const chunk = text.slice(r.start, r.end).trim();
    if (chunk.length > 5) segments.push(chunk);
  }

  // Deduplicate: remove exact-duplicate segments
  const seen = new Set();
  const unique = segments.filter(seg => {
    if (seen.has(seg)) return false;
    seen.add(seg);
    return true;
  });

  const header = `/* ${OUT_NAME} — home-page first-paint CSS extracted from ${TARGET} @ ${VP_WIDTH}x${VP_HEIGHT}
   Generated by scripts/css-extract-critical.js (Playwright CSS Coverage)
   Loaded render-blocking; ${TARGET} demoted to media="print" onload swap.
   REGENERATE after significant home-page markup changes. */\n\n`;

  const output = header + unique.join('\n\n') + '\n';
  fs.writeFileSync(OUT, output, 'utf8');

  console.log(`\nExtracted ${unique.length} rule blocks → ${OUT_NAME}`);
  console.log(`Output: ${(output.length / 1024).toFixed(1)}KB`);
  console.log(`\nNext steps:`);
  console.log(`  1. Review ${OUT_NAME} for completeness`);
  console.log(`  2. Add <link rel="stylesheet" href="${OUT_NAME}"> (blocking)`);
  console.log(`  3. Demote ${TARGET} to media="print" onload swap`);
  console.log(`  4. Add ${OUT_NAME} to sw.js SHELL_ASSETS + build.js CSS_TARGETS`);
  console.log(`  5. Visual verify both themes before shipping\n`);
}

main().catch(e => { console.error(e); process.exit(1); });
